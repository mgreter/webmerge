package OCBNET::Spritesets::Edge;
####################################################################################################

use strict;
use warnings;

####################################################################################################

# a stack is a container is a block
use base 'OCBNET::Spritesets::Stack';

####################################################################################################

# layout the edge
# ******************************************************************************
sub layout
{

	# get instance
	my ($self) = shift;

	# call stack layout
	$self->SUPER::layout;

	# call and return base method
	return $self;

}

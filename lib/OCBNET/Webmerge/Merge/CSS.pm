package OCBNET::Webmerge::Merge::CSS;
################################################################################
# use base 'OCBNET::Webmerge::Merge';
################################################################################

use strict;
use warnings;

################################################################################
# execute main action
################################################################################

sub execute
{
	my ($node, $context) = @_;
	# $node->log("execute MERGE::CSS");
	foreach my $output ($node->find('OUTPUT'))
	{
		STDOUT->autoflush(1);
		$node->logAction('render');
		my $data = $output->render;
		$node->logAction('write');
		my $rv = $output->write($data);
		$node->logSuccess($rv);
	}
}

################################################################################
# some accessor methods
################################################################################

# return node type
sub type { 'INPUT::CSS' }

################################################################################
################################################################################
1;
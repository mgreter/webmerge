package OCBNET::Webmerge::Runner::Execute;
################################################################################

use strict;
use warnings;

################################################################################
# dispatch execute to all childrens
################################################################################
use List::MoreUtils qw(uniq);
################################################################################

# execute all blocks (should be unique)
sub run { $_->execute foreach (uniq @_); }

################################################################################
# register our runner within the main module
################################################################################

OCBNET::Webmerge::Runner::register('action', \&run, 0);

################################################################################
################################################################################
1;

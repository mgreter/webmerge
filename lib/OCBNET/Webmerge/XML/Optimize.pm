package OCBNET::Webmerge::XML::Optimize;
################################################################################
use base qw(OCBNET::Webmerge::Optimize);
use base qw(OCBNET::Webmerge::XML::Tree::Scope);
################################################################################

use strict;
use warnings;

################################################################################
# load additional modules
################################################################################

require OCBNET::Webmerge::XML::Optimize::ANY;

################################################################################
# accessor methods
################################################################################

# return node type
sub type { 'OPTIMIZE' }

################################################################################
# class according to tagname
################################################################################

sub classByTag
{
	warn ref $_[0];
	# only if tag does not match node type
	uc $_[1] eq $_[0]->type ? ref $_[0]
	: join '::', __PACKAGE__, 'ANY'
}

################################################################################

$OCBNET::Webmerge::XML::parser{'optimize'} = __PACKAGE__;

################################################################################
################################################################################
1;

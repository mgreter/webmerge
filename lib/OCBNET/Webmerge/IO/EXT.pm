package OCBNET::Webmerge::IO::EXT;
################################################################################
use base OCBNET::Webmerge::IO::File;
################################################################################

use strict;
use warnings;

################################################################################
# implement file interface
################################################################################

sub path { join('.', $_[0]->parent->path, $_[0]->extension) }

################################################################################
use File::Basename qw();
################################################################################

sub dirname { File::Basename::dirname($_[0]->path) }
sub basename { File::Basename::basename($_[0]->path) }
sub parsefile { File::Basename::basename($_[0]->path) }

################################################################################
# implement config tree interface
################################################################################

sub attr { shift->parent->attr(@_) }

################################################################################
# implement css file interface
################################################################################

sub sheet { shift->parent->sheet(@_) }

################################################################################
# implement additional methods
################################################################################

sub commit {}
sub revert {}
sub collect {}
sub processors { }

################################################################################
# accessor methods
################################################################################

# return node type
sub type { 'EXT' }

################################################################################
# implement some methods
################################################################################

sub importer { return $_[1] }
sub resolve { return $_[1] }


package OCBNET::Plack::App::Echo;
################################################################################
use parent qw(Plack::Component);
################################################################################

use strict;
use warnings;

################################################################################
################################################################################

# echo request
sub call
{

	# psgi environment
	my ($self, $env) = @_;
	# get a request object from psgi
	my $req = Plack::Request->new($env);

	return [ '200',
		[ 'Content-Type' => 'text/plain' ],
		[ Data::Dumper::Dumper($req) ],
	];

}

################################################################################
################################################################################
1;